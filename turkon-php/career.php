<p><strong>Our Human Resources Approach</strong><br>
Our human resources approach is to make the human resources that works in a way to serve our company’s strategies, is goal-oriented by increasing its productivity, works in cooperation, is open to learning and development, can adapt to changes and new conditions quickly, and is agile and highly motivated sustainable in the organisation.</p>

<p><strong>Recruitment and Placement</strong><br>
• In the recruitment and placement process where the “right person for the right job” principle is targeted, candidates who applied through the Turkon Line career portal (Navigation) and other job application platforms are invited to interviews after evaluations.</p>

<p>• Once the interviews are conducted by the Human Resources Department, depending on the requirements of the position candidates are examined with assessment centre, foreign language test, general aptitude tests and personality inventory.&nbsp;</p>

<p>• The eligible candidates are called for an interview with the department’s manager.</p>

<p>• Job offers are sent to those candidates whose process is continuing positively. The adaptation of the candidates who accept the job offer is supported by the orientation trainings and other applications.</p>

<p><a href="https://www.kariyer.net/firma-profil/turkon-konteyner-tasimacilik-a-s-14433-225779" target="_blank">Click </a>to see our open positions.<br>
&nbsp;</p>

<p><strong>Performance management</strong><br>
We support the "Success and Development Route", our target and competency based performance management system with continuous feedback. Measurable targets, which are determined based on the company strategy, are spread throughout the organisation and are realised with the added value provided by our employees. Competency developments that our employees need for sustainable success are monitored throughout the year.<br>
&nbsp;</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp; &nbsp; &nbsp; &nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;<img alt="" src="assets/images/career/turkon-academy_en.jpg" style="width: 300px; height: 98px;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</p>

<p><strong>Training and Development Programs</strong><br>
Turkon Academy has Leadership, Personal, Professional and Technical Development, Sales, My Career at Turkon Programs.</p>

<p><br>
1. Faculty of Leadership; It includes practices that improve the leadership competencies of our employees in managerial and higher positions.<br>
2. Faculty of Personal, Professional and Technical Development;&nbsp;aims to strengthen the professional, technical and personal development of all our employees, regardless of their position. Listen From Me (Internal Trainer Program); Professional and technical development of our teams is ensured through the trainings given by our employees assigned as internal trainers.<br>
3. Faculty of Sales; It aims to develop the competencies of our sales teams within the framework of our "Customer-Specific Solutions" focus.<br>
4. My Career at Turkon; Awareness of our employees is increased with up-to-date Human Resources practices within our company.</p>

<p><strong>Skill Management</strong><br>
The career development of our employees is supported by the “Career Opportunities” application which is our internal advertisement system for our employees who want to advance in different departments or positions.</p>

<p>Our employees who are candidates for managerial roles are appointed to their new positions upon successful completion of various evaluation applications. Their development continues to be supported until they adapt to their new positions.</p>

<p>Career developments of our employees in expert roles are managed in line with the results of target and competency assessments.</p>

<p><strong>Future Team (Long Term Internship Program)</strong><br>
It is a long-term internship program that aims to enable young people studying in the relevant departments of universities to develop their careers by experiencing real work experiences. After the completion of the internship program, those who successfully complete the internship program are evaluated for our open positions in Turkon Group companies with priority and job opportunities are offered.</p>

<p><br>
<strong>Who Can Apply?</strong><br>
• Studying the 3rd year in university (Junior) or graduate student,<br>
• Advanced level of English,<br>
• Being able to work 5 days a week for 3 months during the summer period,<br>
• Being able to work 2 days a week for the winter period,</p>

<p><strong>Evaluation process</strong><br>
• Application (February-March of the relevant year)<br>
• Preliminary assessment<br>
• Examination (English, General Aptitude Test, Personality Inventory)<br>
• Competency Based Interview<br>
• Offer Process<br>
&nbsp;</p>

