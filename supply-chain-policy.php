                            <h2>Supply Chain Policy</h2>
                
                <p>Turkon Holding A.Ş. and its companies (Turkon), aim to ensure that environmental, labour and economic policies are always sustainable by carrying out the business plans developed with all its employees and partners. Turkon will protect and enrich all factors that will contribute to the quality of life of future generations so that tomorrow is better than today.</p>
                
                <p>Turkon Holding A.Ş. and our member companies expect all their suppliers that partake in the provision of service with all of the elements that generate information to adopt this approach.<br>
                In accordance with this policy, we expect all organizations that provide services or goods to us, as well as Turkon Holding and its members, to accept the factors required by this policy with their service contents and practices.</p>
                
                <p></p>
                
                <p><strong>1. Legal Application</strong></p>
                
                <p><strong>Compliance with the Law and Regulations</strong><br>
                All the employees in Turkon and its member companies work with the legal boundaries. It acts and follows up in accordance with local and international laws.<br>
                <strong>Competition Law</strong><br>
                The supplier must work in full accordance with the fair competition and monopoly law which regulate their area of authority.<br>
                <strong>Bribe and Corruption</strong><br>
                The suppliers are expected to show no tolerance to bribe or corruption under any circumstances and to be sensitive on the matter.</p>
                
                <p><strong>2. Working Conditions</strong><br>
                <br>
                <strong>Forced Labour</strong><br>
                The suppliers must only employ consenting labour who work with their own free will. It is expected that our suppliers do not force their employees with false claims, threat, force or any other form of pressure and are sensitive on this matter.<br>
                <strong>Child Labour</strong><br>
                The suppliers must not employ workers under the age that was defined by the legal regulations.<br>
                <strong>Harassment</strong><br>
                Infringement of immunities by physical, sexual, psychological and/or emotional harassment at work or anywhere work-related is against law and ethics code and must not be tolerated in any way.<br>
                <strong>Compensation and Wages</strong><br>
                The wage, overtime and related benefits must be equal to current regulations or more.</p>
                
                <p><strong>Working Hours</strong><br>
                Our suppliers must act in accordance with applicable legislation, particularly working hours and overtime.<br>
                <br>
                <strong>Non-discrimination</strong><br>
                The suppliers must not discriminate on race, colour, origin, sex, religion, disability on any employment related issues including recruitment, promotion, compensation, benefits, training, mandatory layoff and discharge.<br>
                <strong>Union and Freedom of Collective Bargaining</strong><br>
                Employees of the suppliers are free to join legal organisations on their own will. The suppliers must recognise and collective bargaining rights and respect employees’ legal organisations. Employees must not be threatened against joining these organisations.</p>
                
                <p><strong>3. Ethics Rules</strong><br>
                &nbsp;<br>
                A RedLine which is &nbsp;an Ethic Comminication Line has been established so that our customers, suppliers, employees and all stakeholders can reach if there is any observations that are considered to be inconsistent with the Turkon Line Ethical Principles.</p>
                
                <p><strong>4. Workplace Health and Safety</strong></p>
                
                <p>As Turkon Live we are committed to;<br>
                <br>
                Create a safe working environment for all our colleagues while producing services, to anticipate all kinds of health and safety risks that may arise due to work, and to present and apply the necessary protection / protection methods,<br>
                <br>
                Organize trainings for all partners and employees to effectively fulfil workplace health and safety practices and to provide the necessary equipment,<br>
                <br>
                Provide continuous improvements, to encourage all value-added owners for the implementation of applications, and to reach targets by setting measurable and auditable targets.<br>
                <br>
                Our expectations from our suppliers for Workplace Health and Safety are;<br>
                <br>
                • To comply with the legal regulations regarding work health and safety,<br>
                • To create safe and healthy working environments for their employees, interns and visitors<br>
                • To provide all the resources and improvements their employees need.</p>
                
